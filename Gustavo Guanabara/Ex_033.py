print('=== Função Área ===')

# Função que calcula a área
def area(largura, comprimento):
    return largura * comprimento  # multiplica largura pelo comprimento

# Chamando a função e guardando o resultado
resultado = area(10, 25.5)

# Mostrando o resultado com unidade de medida
print(f'Área: {resultado} m²')